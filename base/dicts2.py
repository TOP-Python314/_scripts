d1 = dict(a=1, b=2, c=3)
# >>> d1
# {'a': 1, 'b': 2, 'c': 3}

d2 = dict([(44, 'd'), (55, 'e'), (66, 'f')])
# >>> d2
# {44: 'd', 55: 'e', 66: 'f'}

d3 = dict(('Аа', 'Бб', 'Вв'))
# >>> d3
# {'А': 'а', 'Б': 'б', 'В': 'в'}

d4 = dict.fromkeys(range(2, 10, 2))
# >>> d4
# {2: None, 4: None, 6: None, 8: None}

d5 = dict.fromkeys([1, 3, 5, 7], '')
# >>> d5
# {1: '', 3: '', 5: '', 7: ''}
